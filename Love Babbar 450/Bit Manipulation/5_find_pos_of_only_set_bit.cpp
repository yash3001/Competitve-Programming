class Solution {
  public:
    int findPosition(int n) {
        // code here
        int ans{}, count{};
        while(n>0){
            if(n&1){
                count++;
            }
            ans++;
            n>>=1;
        }
        if(count!=1){
            return -1;
        }
        return ans;
    }
};

