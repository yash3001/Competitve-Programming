template<typename T>
T gcd(T a, T b){
    if(b == 0)
        return a;
    return(gcd<T>(b, a%b));
}

template<typename T>
T lcm(T a, T b){
    return (a / gcd<T>(a, b)) * b;
}

template<typename T>
void swap_(T &a, T &b){
    a = a^b;
    b = b^a;
    a = a^b;
}

ll count(ll n){
    ll c{};
    while(n>0){
        c++;
        n /= 10;
    }
    return c;
}

void solve(){
    ll n; cin>>n;
    ll ans{};
    while(n>0){
        ll c = count(n);
        if(c<4)
            break;
        ll m = 2050*pow(10, c-4);
        if(m>n){
            m = 2050*pow(10, c-5);
        }
        n -= m;
        ans++;
    }
    if(n != 0){
        cout<<-1<<endl;
    }
    else{
        cout<<ans<<endl;
    }
}

